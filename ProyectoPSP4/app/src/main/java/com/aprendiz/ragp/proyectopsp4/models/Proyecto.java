package com.aprendiz.ragp.proyectopsp4.models;

public class Proyecto {
    private int id;
    private String nombre;

    public Proyecto() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
}
